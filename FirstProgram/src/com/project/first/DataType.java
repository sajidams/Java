package com.project.first;

public class DataType {
	public static void main(String[] args)
	{
		byte byteValue=30;
		short shortValue=150;
		int intValue=1000;
		long longValue=8723598l;
		float floatValue=150.54f;
		double doubleValue=140.788d;
		char charValue='s';
		boolean isValid=true;
		String name="Sajida Shaikh";
		String number="92348209134";
		String symbol="#@!@$%#$%@#$";
		String decimal="1231.567";
		String alphaNum="asdfas87798";
		
		System.out.println("The value of byte is: "+byteValue);
		System.out.println("The value of short is: "+shortValue);
		System.out.println("The value of int is: "+intValue);
		System.out.println("The value of long is: "+longValue);
		System.out.println("The value of float is: "+floatValue);
		System.out.println("The value of double is: "+doubleValue);
		System.out.println("The value of char is: "+charValue);
		System.out.println("The value of boolean is: "+isValid);
		System.out.println("The value of name is: "+name);
		System.out.println("The value of number is: "+number);
		System.out.println("The value of symbol is: "+symbol);
		System.out.println("The value of decimal is: "+decimal);
		System.out.println("The value of alphaNumeric is: "+alphaNum);
		
	}

}
